    void fillTable(JTable table, ResultSet rs)
    {
         while ( table.getRowCount() > 0 )
            {
                ((DefaultTableModel)table.getModel()).removeRow(0) ;
            }
           
        try {
            
             int col = rs.getMetaData().getColumnCount() ;
            
            while ( rs.next() )
            {
                int id = rs.getInt("id") ;
                String task = rs.getString("task") ;
                String date = rs.getString("date") ;
                String deadline = rs.getString("deadline") ;
                String pic = rs.getString("PersonInCharge") ;
                String state = rs.getString("state") ;
                
                Object[] o = new Object[]{id,task,date,deadline,pic,state} ;
                ((DefaultTableModel)table.getModel()).addRow(o) ;
            }
            
            
        } catch (Exception ex) {
            
        }
    }
    void loadInitial()
    {   ImportDataControl idc = new ImportDataControl() ;
        ResultSet rs = idc.loadAllTasks() ;
        fillTable(TaskTable,rs);
        int c =0;
        try {
            rs.last();
            c = rs.getRow();
        } catch (SQLException ex) {
            Logger.getLogger(Frame.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        LocalDateTime ldt = LocalDateTime.now() ;
            
            int dd = ldt.getDayOfMonth() ;
            String ddy = String.valueOf( dd ) ;
            if ( dd<10 ) 
                ddy = '0'+ddy ;
            
            int mth = ldt.getMonthValue() ;
            String mmth = String.valueOf(mth) ;
            if ( mth<10 ) 
                mmth = '0'+mmth ;

            String str = String.valueOf( ldt.getYear() )+"-"+mmth+"-"+ddy ;
        
        rs = idc.loadTasksToday(str) ;
        fillTable(ComingSoonTable,rs);
        
        int count =0 ;
        
        rs = idc.taskState("Done") ;
        try {
            rs.last();
            count = rs.getRow();
        } catch (SQLException ex) {
            Logger.getLogger(Frame.class.getName()).log(Level.SEVERE, null, ex);
        }
        int dr = count*100/c;
        DoneRatio.setValue(dr);
        
        rs = idc.taskState("In progress") ;
        try {
            rs.last();
            count = rs.getRow();
        } catch (SQLException ex) {
            Logger.getLogger(Frame.class.getName()).log(Level.SEVERE, null, ex);
        }
        int ipr = count*100/c;
        InProgressRatio.setValue(ipr);
        
        rs = idc.taskState("Not yet") ;
        try {
            rs.last();
            count = rs.getRow();
        } catch (SQLException ex) {
            Logger.getLogger(Frame.class.getName()).log(Level.SEVERE, null, ex);
        }
        int ny = count*100/c;
        NotYetRatio.setValue(ny);
        
    }
   
    
