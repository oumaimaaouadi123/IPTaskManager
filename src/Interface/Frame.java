    private void ComingSoonTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ComingSoonTableMouseClicked
        int r = ComingSoonTable.getSelectedRow() ;
        int id = (int) ComingSoonTable.getValueAt(r, 0) ;
        
        int rc = TaskTable.getRowCount() ;
        int i=0;
        int lid=(int)TaskTable.getValueAt(0,0);
        while( (lid!=id) && (i<=rc) )
        {
            i++ ;
            lid=(int)TaskTable.getValueAt(i,0);
        }
        
        
        TaskTable.setRowSelectionInterval(i, i);
        
        resetColor(HomePanel);
        setColor(TaskManagerPanel);
        resetColor(StatisticsPanel);
        TaskManagerPage.setVisible(true) ;
        HomePage.setVisible(false) ;
        StatisticsPage.setVisible(false);
    }//GEN-LAST:event_ComingSoonTableMouseClicked
     
